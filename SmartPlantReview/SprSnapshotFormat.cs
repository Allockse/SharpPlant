
namespace SharpPlant.SmartPlantReview
{
    /// <summary>
    ///     Controls the output format of snaphot objects.
    /// </summary>
    public enum SprSnapshotFormat
    {
        /// <summary>
        ///     .bmp bitmap image format.
        /// </summary>
        Bmp = 0,

        /// <summary>
        ///     .jpg JPEG image format.
        /// </summary>
        Jpg = 1,

        /// <summary>
        ///     .png Raster image format.
        /// </summary>
        Png = 2,

        /// <summary>
        ///     .pdf Document format.
        /// </summary>
        Pdf = 3
    }
}
